<br/>
Periodontitis cukup jarang terjadi pada orang di bawah usia 30 tahun. Kondisi ini lebih mungkin terjadi seiring bertambahnya usia. Namun, kondisi ini dapat memengaruhi siapa saja dengan kebersihan mulut yang buruk â€” mereka yang tidak menyikat gigi dan membersihkan gigi secara teratur. Beberapa orang juga lebih rentan secara genetik terhadap periodontitis daripada yang lain. Jika orang tua kandung atau kakek-nenek Anda memiliki riwayat penyakit gusi, Anda juga lebih mungkin mengalaminya.
